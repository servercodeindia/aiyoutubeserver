// Get current language from localStorage or default to English
function getCurrentLanguage() {
  return localStorage.getItem('language') || 'en';
}

// Set language
function setLanguage(lang) {
  if (!translations[lang]) lang = 'en';
  localStorage.setItem('language', lang);
  applyTranslations(lang);
}

// Apply translations to the page
function applyTranslations(lang) {
  const t = translations[lang] || translations.en;
  
  // Update hero section
  const heroTitle = document.querySelector('.hero-title');
  if (heroTitle) {
    heroTitle.innerHTML = `${t.hero_title}<br /><span class="gradient-text">${t.hero_subtitle}</span>`;
  }
  
  const heroSub = document.querySelector('.hero-sub');
  if (heroSub) heroSub.textContent = t.hero_platforms;
  
  // Update input placeholder
  const urlInput = document.getElementById('urlInput');
  if (urlInput) urlInput.placeholder = t.input_placeholder;
  
  // Update download button
  const btnText = document.querySelector('.btn-text');
  if (btnText) btnText.textContent = t.btn_download;
  
  // Update TOS text
  const searchTos = document.querySelector('.search-tos');
  if (searchTos) {
    const tosLink = searchTos.querySelector('a');
    const linkHref = tosLink ? tosLink.href : '/terms-of-use';
    searchTos.innerHTML = `${t.tos_text} <a href="${linkHref}">${t.tos_link}</a>`;
  }
  
  // Update navigation links
  const navHome = document.getElementById('nav-home');
  if (navHome) navHome.textContent = t.nav_youtube || 'Video Downloader';
  
  const navText = document.querySelector('.nav-text');
  if (navText) navText.textContent = t.nav_platforms || 'Supports: YouTube, Facebook, Instagram, Twitter, TikTok';
  
  // Update language button text
  const langBtn = document.querySelector('.lang-btn span');
  if (langBtn && translations[lang]) {
    langBtn.textContent = translations[lang].name;
  }
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Store current translations globally for use in app.js
  window.currentTranslations = t;
}

// Build language dropdown with only supported languages
function buildLanguageDropdown() {
  const dropdown = document.getElementById('langDropdown');
  if (!dropdown) return;
  
  // Clear existing items
  dropdown.innerHTML = '';
  
  // Add only supported languages
  Object.keys(translations).forEach(langCode => {
    const li = document.createElement('li');
    const a = document.createElement('a');
    a.href = '#';
    a.setAttribute('data-lang', langCode);
    a.textContent = translations[langCode].name;
    
    // Add click handler
    a.addEventListener('click', (e) => {
      e.preventDefault();
      setLanguage(langCode);
      // Close dropdown
      dropdown.classList.remove('open');
      document.getElementById('langBtn')?.setAttribute('aria-expanded', 'false');
    });
    
    li.appendChild(a);
    dropdown.appendChild(li);
  });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  // Build dropdown with supported languages only
  buildLanguageDropdown();
  
  // Apply current language
  const currentLang = getCurrentLanguage();
  applyTranslations(currentLang);
});
